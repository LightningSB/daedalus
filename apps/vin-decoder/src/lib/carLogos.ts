export function getCarLogo(make: string): string | null {
  if (!make) return null;
  
  const normalizedMake = make.toLowerCase().trim();
  
  // Direct match
  if (CAR_LOGOS[normalizedMake]) {
    return CAR_LOGOS[normalizedMake];
  }
  
  // Try partial match
  for (const [key, url] of Object.entries(CAR_LOGOS)) {
    if (normalizedMake.includes(key) || key.includes(normalizedMake)) {
      return url;
    }
  }
  
  return null;
}

export function getAllMakes(): string[] {
  return Object.keys(CAR_LOGOS);
}
